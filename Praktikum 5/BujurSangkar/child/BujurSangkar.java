// Deskripsi : class BujurSangkar dimana terdapat metode untuk menghitung luas yang mengambil dari abstract class BangunDatar

public class BujurSangkar extends BangunDatar {
    public double hitungLuas (double sisi) {
        luas = sisi*sisi;
        return luas;
    }
}
